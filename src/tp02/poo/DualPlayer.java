    private void jogarPvp(){//Função responsável pela Inicialização do jogo
        jogadorAtual.setText(nome1+", Sua vez:");
        nivelJogo = 0;
        nivelUsuario = 0; //Iniciando as variaveis como para iniciar o jogo
        jogador = 0;
        switch(this.dificuldade){//switch para relacionar a dificuldade com o nivel maximo de cada uma
                case 1 -> nivelMax = 8;
                case 2 -> nivelMax = 14;
                case 3 -> nivelMax = 20;
                case 4 -> nivelMax = 31;
                case 5 -> nivelMax = -1;//Foi utilizado o -1 para modo infinito pois como a variavel comparativa incrementa pra cima, nunca vai ter critério de parada
            }
    }
    
    private void funcaoJogar(int valor){//Função responsavel pelo fluxo do jogo
        if(jogador == 0){                       //Tratamento diferente para cada jogador
            if(nivelJogo == nivelUsuario){      //caso o usuario tenha digitado a sequencia toda
                adicionarJogada(valor);         //Adiciona o novo valor
                nivelUsuario = 0;               //volta para a iteração 0
                jogador++;                      //passa para o próximo jogador
                jogadorAtual.setText(nome2+", Sua vez:");
            }else{
                switch(verificaJogada(valor)){  
                    case 1 -> nivelUsuario++;   //caso o usuario tenha acertado, passa para a próxima incrementação
                    case 0 -> finaliza(1); //caso o usuario tenha errado
                }
            }
        }else{
            if(verificaJogada(valor) == 0){
                finaliza(0);
            }else if(verificaJogada(valor) == 1){
                if(nivelJogo == nivelUsuario){
                    nivelJogo++;
                    nivelUsuario = 0;
                    //caso de empate
                    if(nivelJogo == nivelMax){
                        finaliza(2);
                        return;
                    }
                    jogador--;
                    jogadorAtual.setText(nome1+", Sua vez:");
                }else{
                    nivelUsuario++;
                }
            }
        }
    }
    
    private void playWithKey(java.awt.event.KeyEvent evt)//Função responsável pela utilização do teclado
    {
        char keyPressed = evt.getKeyChar();
        funcaoJogar(getColorFromKey(keyPressed));
    }
    
    private int getColorFromKey(char key){//Função de conversão das teclas em valores
        if(key == 'w' || key == 'W'){ //vermelho
           acaoVermelho();
           return 3;
        }
        
        if(key == 'e' || key == 'E'){ //verde
            acaoVerde();
            return 1;
        }
        
        if(key == 's' || key == 'S'){ //azul
            acaoAzul();
            return 2;
        }
        
        if(key == 'd' || key == 'D'){ //amarelo
            acaoAmarelo();
            return 4;
        }
        return -1;
        
    }
    private int verificaJogada(int valor){
        //compara se os valores são iguais
        if(listNumber.get(nivelUsuario) != valor){
            return 0;
        }
        return 1;
    }
    
    private void adicionarJogada(int valor){//Adiciona jogada ao fim do vetor
        listNumber.add(valor); 
    }    
    
