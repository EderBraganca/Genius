    public void finaliza(int i){
        this.setEnabled(false);
        this.setVisible(false);
        
        menu.setEnabled(true);
        menu.setVisible(true);
        
        switch(i){
            case 0 -> resultado = new Resultado(nome1, 1);
            case 1 -> resultado = new Resultado(nome2, 1);
            case 2 -> resultado = new Resultado(nome1, nome2);
            }
    }
    
